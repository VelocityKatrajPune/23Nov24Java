package loops;

public class Test1 {

	public static void main(String[] args) {
		
		int x=9;
		System.out.println(x);
		x++;//post increment
		System.out.println(x);
		
		int y=1;
		System.out.println(y);
		--y;//pre decrement
		System.out.println(y);
		
		System.out.println("======================");
		int p=10;
		int q=++p;//pre increment
		System.out.println(p);
		System.out.println(q);
		System.out.println("======================");
		int r=10;
		int s=r++;// post increment
		System.out.println(r);
		System.out.println(s);
		
		

	}

}
