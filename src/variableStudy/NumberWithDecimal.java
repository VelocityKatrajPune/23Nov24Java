package variableStudy;

public class NumberWithDecimal {

	public static void main(String[] args) {
		
		//I want to store my weight 99.9, 55.3, 80.0
     // Number with decimal-->float, double
		
		float weight;//1. variable declartion
		weight=99.99f;//2. variable initization
		System.out.println("My weight is "+weight);
		
		// I want store cricket overs 4.5, 6, 10, 10.2
		
		float overs;// variable declare
		//overs=10;// variable initialization
		overs=10.5f;
		System.out.println("My overs are "+overs);
		
		
		//10.12
		//10.000001
		
	}

}
